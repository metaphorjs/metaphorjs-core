
const MetaphorJs = require( "../MetaphorJs");

module.exports = MetaphorJs.lib.Cache = (function(){

    var globalCache;

    /**
     * @class MetaphorJs.lib.Cache
     */

    /**
     * @method
     * @constructor
     * @param {bool} cacheRewritable
     */
    var Cache = function(cacheRewritable) {

        var storage = {},

            finders = [];

        if (arguments.length == 0) {
            cacheRewritable = true;
        }

        return {

            /**
             * Add finder function. If cache doesn't have an entry
             * with given name, it calls finder functions with this
             * name as a parameter. If one of the functions
             * returns anything else except undefined, it will
             * store this value and return every time given name
             * is requested.
             * @param {function} fn {
             *  @param {string} name
             *  @param {Cache} cache
             *  @returns {* | undefined}
             * }
             * @param {object} context
             * @param {bool} prepend Put in front of other finders
             */
            addFinder: function(fn, context, prepend) {
                finders[prepend? "unshift" : "push"]({fn: fn, context: context});
            },

            /**
             * Add cache entry
             * @method
             * @param {string} name
             * @param {*} value
             * @param {bool} rewritable
             * @returns {*} value
             */
            add: function(name, value, rewritable) {

                if (storage[name] && storage[name].rewritable === false) {
                    return storage[name];
                }

                storage[name] = {
                    rewritable: typeof rewritable != "undefined" ? 
                                    rewritable : cacheRewritable,
                    value: value
                };

                return value;
            },

            /**
             * Get cache entry
             * @method
             * @param {string} name
             * @param {*} defaultValue {
             *  If value is not found, put this default value it its place
             * }
             * @returns {* | undefined}
             */
            get: function(name, defaultValue) {

                if (!storage[name]) {
                    if (finders.length) {

                        var i, l, res,
                            self = this;

                        for (i = 0, l = finders.length; i < l; i++) {

                            res = finders[i].fn.call(finders[i].context, name, self);

                            if (res !== undefined) {
                                return self.add(name, res, true);
                            }
                        }
                    }

                    if (defaultValue !== undefined) {
                        return this.add(name, defaultValue);
                    }

                    return undefined; 
                }

                return storage[name].value;
            },

            /**
             * Remove cache entry
             * @method
             * @param {string} name
             * @returns {*}
             */
            remove: function(name) {
                var rec = storage[name];
                if (rec && rec.rewritable === true) {
                    delete storage[name];
                }
                return rec ? rec.value : undefined;
            },

            /**
             * Check if cache entry exists
             * @method
             * @param {string} name
             * @returns {boolean}
             */
            exists: function(name) {
                return !!storage[name];
            },

            /**
             * Walk cache entries
             * @method
             * @param {function} fn {
             *  @param {*} value
             *  @param {string} key
             * }
             * @param {object} context
             */
            eachEntry: function(fn, context) {
                var k;
                for (k in storage) {
                    fn.call(context, storage[k].value, k);
                }
            },

            /**
             * Clear cache
             * @method
             */
            clear: function() {
                storage = {};
            },

            /**
             * Clear and destroy cache
             * @method
             */
            $destroy: function() {

                var self = this;

                if (self === globalCache) {
                    globalCache = null;
                }

                storage = null;
                cacheRewritable = null;

                self.add = null;
                self.get = null;
                self.destroy = null;
                self.exists = null;
                self.remove = null;
            }
        };
    };

    /**
     * Get global cache
     * @method
     * @static
     * @returns {Cache}
     */
    Cache.global = function() {

        if (!globalCache) {
            globalCache = new Cache(true);
        }

        return globalCache;
    };

    return Cache;
    
}());

